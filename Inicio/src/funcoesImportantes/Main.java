package funcoesImportantes;

public class Main {
	public static void main(String[] args) {

		double x = -2.0;
		double y = 4.0;

		double A, B, C;

		A = Math.sqrt(y); // raiz quadrada de y
		B = Math.pow(x, y); // potenciacao de x elevado a y
		C = Math.abs(x); // valor absoluto de x

		System.out.println(A);
		System.out.println(B);
		System.out.println(C);

	}
}
